% --- Executes just before featureextraction_image is made visible.
function featureextraction_image_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to featureextraction_image (see VARARGIN)

% Choose default command line output for featureextraction_image
handles.output = hObject;
clc;
warning off;

global Image;
global seg_image;
global a3;
global F5;
global Fc;
global F4;

GLCM2 = graycomatrix(F5,'Offset',[0 1; -1 1; -1 0; -1 -1]);                 % Create gray-level co-occurrence matrix from image
stats = GLCM_Features1(GLCM2,0);                                            % Find out GLCM features in this
t1= struct2array(stats);                                                    % Convert this structure to an array
test_feat(:,:) = t1;                                                        % Make feature vector of all images

contrast_disp = stats.contr(1);                                             % Get Contrast 
set(handles.edit1,'string',contrast_disp);                                  % Display in editbox

variance_disp = stats.sosvh(1);                                             % Get Variance
set(handles.edit2,'string',variance_disp);                                  % Display in editbox

mean_disp = stats.savgh(1);                                                 % Get Mean
set(handles.edit3,'string',mean_disp);                                      % Display in editbox

homogeneity_disp = stats.homop(1);                                          % Get Homogeneity
set(handles.edit4,'string',homogeneity_disp);                               % Display in editbox

entropy_disp = stats.entro(1);                                              % Get Entropy
set(handles.edit5,'string',entropy_disp);                                   % Display in editbox

correlation_disp = stats.corrp(1);                                          % Get correlation
set(handles.edit6,'string',correlation_disp);                               % Display in editbox



load 'featureD_dft22.mat'                                                   % Load trained feature ".mat" file
l = [1;2;3];                                                                % Define labels: 1 - Benign, 2 - Malignant, 3 - Normal
label_train = [repmat(l(1),280,1);repmat(l(2),280,1);repmat(l(3),280,1)];   % Repeat labels for training features
class = multisvm1(featureD_dft22,label_train,test_feat);                    % Using multisvm, classify image

% Depending on the labels, we will get the output and hence will display
% that in textbox
if class == 1
    out_disp = 'BENIGN'
    set(handles.edit7,'string',out_disp);  
elseif class == 2
    out_disp = 'MALIGNANT'
    set(handles.edit7,'string',out_disp);  
elseif class == 3
    out_disp = 'NORMAL'
    set(handles.edit7,'string',out_disp);  
end


axes(handles.axes1);
imshow(Image);

axes(handles.axes2);
imshow(a3);

axes(handles.axes3);
imshow(Fc);

axes(handles.axes4);
imshow(F4);

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes featureextraction_image wait for user response (see UIRESUME)
% uiwait(handles.figure1);



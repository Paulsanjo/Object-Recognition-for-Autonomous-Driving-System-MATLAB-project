% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
set(handles.pushbutton1,'Enable','Off');
set(handles.pushbutton2,'Enable','On');
set(handles.pushbutton3,'Enable','On');
set(handles.pushbutton4,'Enable','On');

global Image;

[fname, pthname] = uigetfile('*.*', 'Select the Test image');               % Ask user to select test image
dataset_fullpath = strcat(pthname, fname);                                  % Concatenate path and image name 
Image = imread(dataset_fullpath);                                           % Read complete image with path
Image = im2uint8(Image);                                                    % Convert datatype
Image = imresize(Image,[1000,667]);                                         % Resize it

set(handles.edit1,'string',dataset_fullpath);                               % Write full path of the file to edit1 box

% Set the variable with handles to use it in all other pushbuttons (to declare globally)
handles.Image = Image;
% Store this data (handles variables) in gui data
guidata(hObject, handles);



